# PHYTON VARIABLE ASSIGN MULTIPLE VALUES

# MANY VALUES TO MULTIPLE VARIABLES
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)
print()

# ONE VALUE TO MULTIPLE VARIABLE
x = y = z = "Orange"
print(x)
print(y)
print(z)
print()

# UNPACK A COLLECTION
fruits = ['apple', 'banana', 'cherry']
x, y, z = fruits
print(x)
print(y)
print(z)
print()
