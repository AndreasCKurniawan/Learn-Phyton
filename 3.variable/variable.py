# VARIABLE

# CREATING VARIABLE
x = 5
y = "John"

print(x)
print(y)

# variable do not need to be declared with any particular type
# and can be changed anytime
x = 4       # INT
x = "Sally" # STRING

# CASTING
# used to spesify data type of a variable
x = str(3)    # x will be "3" STRING
x = int(3)    # x will be 3 INT
x = float(3)  # x will be 3.0 FLOAT

# GET THE TYPE
# use command "type(_target_)"
print()
print(type(x))

