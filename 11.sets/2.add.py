# ADD
# To add one item to a set use the add() method.
mySet = {'Andreas', 'Caesar', 'Kurniawan'}
mySet.add('Seanard')
print(mySet)

# ADD SETS
# To add items from another set or another iretable into the current set, use the update() method.
color = {'black', 'yellow', 'red', 'white'}
mySet.update(color)
print(mySet)
